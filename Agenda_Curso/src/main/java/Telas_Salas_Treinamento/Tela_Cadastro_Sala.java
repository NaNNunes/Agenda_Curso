    private void Jbtn_Cadastrar_CadSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbtn_Cadastrar_CadSalaActionPerformed
        Connection connection = null;
        PreparedStatement statement = null;

        String url = "jdbc:mysql://localhost:3306/db_agenda_curso";
        String user = "root";
        String psswrd = "";

        try {
            connection = DriverManager.getConnection(url, user, psswrd);
            String query = "INSERT INTO localidade(nome) values(?)"; // adicionar tambem id instrutor
            statement = connection.prepareStatement(query);

            statement.setString(1, Jtxtf_Nome_CadSala.getText());

            statement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Sala de Treinamento Cadastrada");
        } catch (SQLException erro){
            JOptionPane.showMessageDialog(null, "Erro: " + erro.getMessage());
        }
    }//GEN-LAST:event_Jbtn_Cadastrar_CadSalaActionPerformed

