    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Connection connection = null;
        PreparedStatement statement = null;

        String url = "jdbc:mysql://localhost:3306/db_agenda_curso";
        String user = "root";
        String psswrd = "";

        try {
            connection = DriverManager.getConnection(url, user, psswrd);
            String query = "INSERT INTO usuario(login_usuario, senha, tipo_usuario) values(?, ?, ?)";
            statement = connection.prepareStatement(query);

            statement.setString(1, jTextField2.getText());
            statement.setString(2, jTextField1.getText());
            statement.setString(3, (String) jComboBox1.getSelectedItem());

            statement.execute();
            JOptionPane.showMessageDialog(null, "Usuario Criado");
            Tela_Login Tela_Login = new Tela_Login();
            Tela_Login.setVisible(true);
            this.dispose();

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Verifique se todos os campos est√£o preenchiodos corretamente!");
            System.out.println(erro);
            System.out.println("Erro: " + erro.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

